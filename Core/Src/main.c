/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

	//Defining the Morse-Code Code for A-Z in English Morse-Code.
	//0=dot,1=dash,read right-to-left
	const unsigned long a_table[36] =
	{
		0b110100,		//A
		0b1100000001,	//B
		0b1100010001,	//C
		0b11000001,		//D
		0b1100,		//E
		0b1100010000,	//F
		0b11000101,		//G
		0b1100000000,	//H
		0b110000,		//I
		0b1101010100,	//J
		0b11010001,		//K
		0b1100000100,	//L
		0b110101,		//M
		0b110001,		//N
		0b11010101,		//O
		0b1100010100,	//P
		0b1101000101,	//Q
		0b11000100,		//R
		0b11000000,		//S
		0b1101,			//T
		0b11010000,		//U
		0b1101000000,	//V
		0b11010100,		//W
		0b1101000001,	//X
		0b1101010001,	//Y
		0b1100000101,	//Z
	};

	//NOTE: ONLY UPPERCASE ONLY MESSAGES
	char msg[] = "OMAR";
	int  msglen = strlen(msg);
	int morse_time_unit = 500;
	int word_spacing_time_scale = 7;
	int letter_spacing_time_scale = 3;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_I2S3_Init();
  MX_SPI1_Init();
  MX_USB_HOST_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    MX_USB_HOST_Process();

    /* USER CODE BEGIN 3 */
    //initial delay before transmitting code
    HAL_Delay(500);

	int i = 0;
	int k = 0;
	for (i = 0; i <= msglen; i++){
		if (msg[i] == '\0') {
			// if the current character has ended then exit.
			// ending indicated by 10seconds of RED LED (PD14)
			HAL_GPIO_WritePin(MORSE_STRING_END_GPIO_Port, MORSE_STRING_END_Pin, GPIO_PIN_SET);
			HAL_Delay(10000);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_RESET);
			break;
		}else if (msg[i] == ' '){
			// if the current character has space, wait word-spacing time units
			out_spacing(morse_time_unit, word_spacing_time_scale);
		}else{
			// if the current character is A~Z, translate to Morse-Code
			int temp = msg[i]-65;
			while (1){
				//0 = dot
				//1 = dash
				//F = exit condition
				int hex_digit = (a_table[temp] & ( 0b11 << 2*k )) >> 2*k;
				if (hex_digit == 0xf){
					out_spacing(morse_time_unit,letter_spacing_time_scale);
					break;
				}else if (hex_digit == 0b01){
					//dash flash
					out_dash(morse_time_unit);
					//letter spacing
					out_spacing(morse_time_unit,1);
				}else if (hex_digit == 0b00){
					//dot flash
					out_dot (morse_time_unit);
					//letter spacing
					out_spacing(morse_time_unit,1);
				}else{
					//there is an error if we are here !!
					break;
				}
				k = k + 1;
			}
			k = 0;
		}
	}
  }
  /* USER CODE END 3 */
}

